	private void addBangMonHoc() throws SQLException {
		try {
			Connection conn = DBconnect.getConnection();
			String sql = "Select * from MonHoc";
			Statement st = conn.createStatement();
			ResultSet rs = st.executeQuery(sql);
			arr_MonHoc.clear();
			while (rs.next()) {
				String MaMH = rs.getString(1);
				String TenMH = rs.getString(2);
				int SoTC = rs.getInt(3);
				MonHoc mh = new MonHoc(MaMH, TenMH, SoTC);
				arr_MonHoc.add(mh);
			}
			conn.close();
		} catch (SQLException ex) {
			System.out.println(ex);
		}
		DefaultTableModel model = (DefaultTableModel) jTableMonHoc.getModel();
		model.setRowCount(0);
		for (MonHoc mh : arr_MonHoc) {
			model.addRow(new Object[] { mh.getMaMH(), mh.getTenMH(), mh.getSoTC() });
		}
	}

